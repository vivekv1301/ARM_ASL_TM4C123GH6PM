; Multiply 2 numbers without using the MUL instruction
		AREA  TEST,CODE,READONLY
		ENTRY
		LDR R0,=0X40000000 ;ADDRESS OF MULTIPLICAND
		LDR R1,=0X40000004 ;ADDRESS OF MULTIPLIER
		LDR R2,[R0] ;LOAD MULTIPLICAND
		LDR R3,[R1] ;LOAD MULTIPLIER
		MOV R4,#00000000; PRODUCT REGISTER
LOOP	ADD R4,R4,R2
		SUBS R3,R3,#1
		BNE LOOP
STOP	B STOP
		END
